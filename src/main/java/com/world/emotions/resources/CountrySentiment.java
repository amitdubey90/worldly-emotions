package com.world.emotions.resources;

public class CountrySentiment {

	private int id;
	private int score;
	
	public CountrySentiment()
	{
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

		
}
